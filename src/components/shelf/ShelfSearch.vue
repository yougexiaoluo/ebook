<template>
  <div class="shelf-search-wrapper" :class="{'search-top': ifInputClicked}">
    <div class="shelf-search" :class="{'search-top': ifInputClicked}">
      <!-- 搜索条 -->
      <div class="search-wrapper">
        <div class="icon-search-wrapper">
          <span class="icon-search icon"></span>
        </div>
        <div class="search-input-wrapper">
          <input type="text"
                 class="search-input"
                 :placeholder="$t('shelf.search')"
                 @click="onSearchClick"
                 v-model="searchText"
          />
        </div>
        <div class="icon-clear-wrapper"
             @click="clearSearchText"
             v-show="searchText.length > 0"
        >
          <span class="icon-close-circle-fill"></span>
        </div>
      </div>
      <!-- 语言图标 -->
      <div class="icon-loccle-wrapper"
           v-if="!ifInputClicked"
           @click="switchLang"
      >
        <span class="icon-cn icon" v-if="lang == 'cn'"></span>
        <span class="icon-en icon" v-else></span>
      </div>
      <!-- 取消按钮 -->
      <div class="cancel-btn-wrapper"
           v-if="ifInputClicked"
           @click="onCancleClick"
      >
        <span class="cancel-text">{{ $t('shelf.cancel') }}</span>
      </div>
    </div>
    <transition name="hot-search-move">
      <div class="shelf-search-tab-wrapper"
           :class="{'hide-shadow': ifHideShadow}"
           v-if="ifInputClicked"
      >
        <div class="shelf-search-tab-item"
             v-for="item in tabs"
             :key="item.id"
             @click="onTabClick(item.id)"
        >
          <span class="shelf-search-tab-text"
                :class="{'is-selected': item.id === selectedTab}"
          >
            {{ item.text }}
          </span>
        </div>
      </div>
    </transition>
  </div>
</template>

<script>
import { setLocalStorage } from '@/utils/localStorage'
import { storeShelfMixin } from '@/utils/mixin/ebookMixin'

export default {
  mixins: [storeShelfMixin],
  computed: {
    lang () {
      return this.$i18n.locale
    },
    tabs () {
      return [
        {
          id: 1,
          text: this.$t('shelf.default')
        },
        {
          id: 2,
          text: this.$t('shelf.progress')
        },
        {
          id: 3,
          text: this.$t('shelf.purchase')
        }
      ]
    }
  },
  watch: {
    offsetY (offsetY) {
      // 显示
      if (offsetY > 0 && this.ifInputClicked) {
        this.ifHideShadow = false
      } else {
        // 不显示
        this.ifHideShadow = true
      }
    }
  },
  data () {
    return {
      ifInputClicked: false,
      searchText: '',
      selectedTab: 1,
      ifHideShadow: true
    }
  },
  methods: {
    onSearchClick () {
      this.ifInputClicked = true
      this.setShelfTitleVisible(false)
    },
    onCancleClick () {
      this.ifInputClicked = false
      this.setShelfTitleVisible(true)
    },
    // 切换语言
    switchLang () {
      if (this.lang === 'cn') {
        this.$i18n.locale = 'en'
      } else {
        this.$i18n.locale = 'cn'
      }
      setLocalStorage('locale', this.$i18n.locale)
    },
    // 清空输入框内容
    clearSearchText () {
      this.searchText = ''
    },
    // 切换tab
    onTabClick (id) {
      this.selectedTab = id
    }
  }
}
</script>

